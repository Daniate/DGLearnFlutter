  AnimationController _createAnAnimationController() {
    return AnimationController(
      duration: const Duration(milliseconds: 300),
      vsync: this,
    );
  }

  Animation<double> _createAnAnimationWithController(
      AnimationController controller) {
    return CurvedAnimation(
      parent: controller,
      curve: Curves.easeInCubic,
    );
  }

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addObserver(this);

    // 初始化与动画相关的对象
    _flashModeControlRowAnimationController = _createAnAnimationController();
    _flashModeControlRowAnimation = _createAnAnimationWithController(
        _flashModeControlRowAnimationController);

    _exposureModeControlRowAnimationController = _createAnAnimationController();
    _exposureModeControlRowAnimation = _createAnAnimationWithController(
        _exposureModeControlRowAnimationController);

    _focusModeControlRowAnimationController = _createAnAnimationController();
    _focusModeControlRowAnimation = _createAnAnimationWithController(
        _focusModeControlRowAnimationController);
  }

  @override
  void dispose() {
    WidgetsBinding.instance.removeObserver(this);
    _flashModeControlRowAnimationController.dispose();
    _exposureModeControlRowAnimationController.dispose();
    _focusModeControlRowAnimationController.dispose();
    super.dispose();
  }

  // App生命周期改变时，可能需要销毁CameraController
  @override
  void didChangeAppLifecycleState(AppLifecycleState state) {
    final CameraController? cameraController = controller;

    // App state changed before we got the chance to initialize.
    if (cameraController == null || !cameraController.value.isInitialized) {
      return;
    }

    if (state == AppLifecycleState.inactive) {
      cameraController.dispose();
    } else if (state == AppLifecycleState.resumed) {
      onNewCameraSelected(cameraController.description);
    }
  }

